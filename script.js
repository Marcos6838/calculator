function add(num1, num2){
    let answer = num1 + num2;
    return answer;
}

function subtract(num1, num2) {
    let answer = num1 - num2;
    return answer;
}

function multiply(num1, num2) {
    let answer = num1 * num2;
    return answer;
}

function divide(num1, num2) {
    let answer = num1 / num2;
    return answer;
}

console.log(add(5, 5));
console.log(subtract(10, 13))
console.log(multiply(5, 5))
console.log(divide(100,10))